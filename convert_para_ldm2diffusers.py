def convert_unet_state_dict(unet_state_dict):
    # print(unet_state_dict.keys())
    # import pdb
    # pdb.set_trace()
    # buyer beware: this is a *brittle* function,
    # and correct output requires that all of these pieces interact in
    # the exact order in which I have arranged them.
    mapping = {k: k for k in unet_state_dict.keys()} #SD
    # for sd_name, hf_name in unet_conversion_map:
    #     # reverse
    #     mapping[sd_name] = hf_name
    for k, v in mapping.items():
        if "resnets" in k:
            for sd_part, hf_part in unet_conversion_map_resnet:
                v = v.replace(sd_part, hf_part)
            mapping[k] = v
    for k, v in mapping.items():
        for sd_part, hf_part in unet_conversion_map_layer:
            v = v.replace(sd_part, hf_part)
        mapping[k] = v
    new_state_dict = {v: unet_state_dict[k] for k, v in mapping.items()}
    return new_state_dict

def convert_unet_gligen2diffuser_pros(unet_state_dict):
    mapping = {k: k for k in unet_state_dict.keys()}
    for k, v in mapping.items():
        if 'fuser' in k:
            for gligen_part, hf_pros_part in unet_conversion_map_gligen:
                v = v.replace(gligen_part, hf_pros_part)
            mapping[k] = v
    new_state_dict = {v: unet_state_dict[k] for k, v in mapping.items()}

    return new_state_dict

